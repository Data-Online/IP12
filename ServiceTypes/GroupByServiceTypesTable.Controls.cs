        public virtual WhereClause CreateWhereClause()
        {
            // This CreateWhereClause is used for loading the data.
            ServiceTypesTable.Instance.InnerFilter = null;
            WhereClause wc = new WhereClause();
    
            // CreateWhereClause() Compose the WHERE clause consist of:
            // 1. Static clause defined at design time.
            // 2. User selected search criteria.
            // 3. User selected filter criteria.
            
        
            if (MiscUtils.IsValueSelected(this.ServiceTypeFilter)) {
                        
                wc.iAND(ServiceTypesTable.ServiceType, BaseFilter.ComparisonOperator.EqualsTo, MiscUtils.GetSelectedValue(this.ServiceTypeFilter, this.GetFromSession(this.ServiceTypeFilter)), false, false);
                    
            }
                      
            if (MiscUtils.IsValueSelected(this.ServiceTypesSearch)) {
                if (this.ServiceTypesSearch.Text == BaseClasses.Resources.AppResources.GetResourceValue("Txt:SearchForEllipsis", null) ) {
                        this.ServiceTypesSearch.Text = "";
                } else {
                  // Strip "..." from begin and ending of the search text, otherwise the search will return 0 values as in database "..." is not stored.
                  if (this.ServiceTypesSearch.Text.StartsWith("...")) {
                      this.ServiceTypesSearch.Text = this.ServiceTypesSearch.Text.Substring(3,this.ServiceTypesSearch.Text.Length-3);
                  }
                  if (this.ServiceTypesSearch.Text.EndsWith("...")) {
                      this.ServiceTypesSearch.Text = this.ServiceTypesSearch.Text.Substring(0,this.ServiceTypesSearch.Text.Length-3);
                      // Strip the last word as well as it is likely only a partial word
                      int endindex = this.ServiceTypesSearch.Text.Length - 1;
                      while (!Char.IsWhiteSpace(ServiceTypesSearch.Text[endindex]) && endindex > 0) {
                          endindex--;
                      }
                      if (endindex > 0) {
                          this.ServiceTypesSearch.Text = this.ServiceTypesSearch.Text.Substring(0, endindex);
                      }
                  }
                }
                string formatedSearchText = MiscUtils.GetSelectedValue(this.ServiceTypesSearch, this.GetFromSession(this.ServiceTypesSearch));
                // After stripping "..." see if the search text is null or empty.
                if (MiscUtils.IsValueSelected(this.ServiceTypesSearch)) {
                      
                    // These clauses are added depending on operator and fields selected in Control's property page, bindings tab.
                  
                    WhereClause search = new WhereClause();
                    
        ColumnList cols = new ColumnList();
      
      cols.Add(ServiceTypesTable.ServiceType, true);
      
      foreach(BaseColumn col in cols)
      {
      
          search.iOR(col, BaseFilter.ComparisonOperator.Contains, MiscUtils.GetSelectedValue(this.ServiceTypesSearch, this.GetFromSession(this.ServiceTypesSearch)), true, false);
        
      }
    
                    wc.iAND(search);
                  
                }
            }
                       
            return wc;
        }
        
         
